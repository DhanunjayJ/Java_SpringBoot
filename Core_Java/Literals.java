class Literals {
    public static void main(String[] args) {
        //literals
        int num1 = 585;
        //base 2
        int num2  = 0b101;
        //hexa decimal
        int num3 = 0x7E;
        //counting number of zeros to diff
        int num4 = 10_00_00_000;
        //double
        double num5 = 12e10;
        System.out.println(num5);
    }
}